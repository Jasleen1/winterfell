use super::CYCLE_LENGTH;
use prover::math::field::{self, add, exp, mul};

const STATE_WIDTH: usize = 4;

pub fn apply_round(state: &mut [u128], step: usize) {
    // determine which round constants to use
    let ark = ARK[step % CYCLE_LENGTH];

    // apply first half of Rescue round
    apply_sbox(state);
    apply_mds(state);
    add_constants(state, &ark, 0);

    // apply second half of Rescue round
    apply_inv_sbox(state);
    apply_mds(state);
    add_constants(state, &ark, STATE_WIDTH);
}

#[inline(always)]
#[allow(clippy::needless_range_loop)]
pub fn add_constants(state: &mut [u128], ark: &[u128], offset: usize) {
    for i in 0..STATE_WIDTH {
        state[i] = add(state[i], ark[offset + i]);
    }
}

#[inline(always)]
#[allow(clippy::needless_range_loop)]
pub fn apply_sbox(state: &mut [u128]) {
    for i in 0..STATE_WIDTH {
        state[i] = exp(state[i], ALPHA);
    }
}

#[inline(always)]
#[allow(clippy::needless_range_loop)]
pub fn apply_inv_sbox(state: &mut [u128]) {
    // TODO: optimize
    for i in 0..STATE_WIDTH {
        state[i] = exp(state[i], INV_ALPHA);
    }
}

#[inline(always)]
#[allow(clippy::needless_range_loop)]
pub fn apply_mds(state: &mut [u128]) {
    let mut result = [field::ZERO; STATE_WIDTH];
    let mut temp = [field::ZERO; STATE_WIDTH];
    for i in 0..STATE_WIDTH {
        for j in 0..STATE_WIDTH {
            temp[j] = mul(MDS[i * STATE_WIDTH + j], state[j]);
        }

        for j in 0..STATE_WIDTH {
            result[i] = add(result[i], temp[j]);
        }
    }
    state.copy_from_slice(&result);
}

#[inline(always)]
#[allow(clippy::needless_range_loop)]
pub fn apply_inv_mds(state: &mut [u128]) {
    let mut result = [field::ZERO; STATE_WIDTH];
    let mut temp = [field::ZERO; STATE_WIDTH];
    for i in 0..STATE_WIDTH {
        for j in 0..STATE_WIDTH {
            temp[j] = mul(INV_MDS[i * STATE_WIDTH + j], state[j]);
        }

        for j in 0..STATE_WIDTH {
            result[i] = add(result[i], temp[j]);
        }
    }
    state.copy_from_slice(&result);
}

